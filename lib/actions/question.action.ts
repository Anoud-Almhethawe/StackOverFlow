"use server";
import { FilterQuery } from "mongoose";

import { revalidatePath } from "next/cache";
import Question from "../../database/Question.model";
import Tag from "../../database/tag.model";
import { connectToDatabase } from "../mongoose";
import {
  CreateQuestionParams,
  DeleteQuestionParams,
  EditQuestionParams,
  GetQuestionByIdParams,
  GetQuestionsParams,
  QuestionVoteParams,
} from "./shared.types";
import User from "../../database/User.model";
import Answer from "@/database/Answer.model";
import Interaction from "@/database/Interaction.model";
import escapeStringRegexp from "escape-string-regexp";

export async function getQuestions(params: GetQuestionsParams) {
  try {
    connectToDatabase();

    const { searchQuery, filter, page = 1, pageSize = 2 } = params;

    // claculate the number of posts to skip based on the page number and pagesize
    const skipAmount = (page - 1) * pageSize;
    const query: FilterQuery<typeof Question> = {};

    if (searchQuery) {
      const escapedSearchQuery = escapeStringRegexp(searchQuery).replace(
        /[*?]/g,
        "\\$&"
      ); // Escape '*' and '?' characters
      query.$or = [
        { title: { $regex: new RegExp(escapedSearchQuery, "i") } },
        { content: { $regex: new RegExp(escapedSearchQuery, "i") } },
      ];
    }
    let sortOptions = {};
    switch (filter) {
      case "newest":
        sortOptions = { createdAt: -1 };
        break;

      case "frequent":
        sortOptions = { views: -1 };
        break;
      case "unanswered":
        query.answers = { $size: 0 };

        break;

      default:
        break;
    }

    const questions = await Question.find(query)
      .populate({
        path: "tags",
        model: Tag,
      })
      .populate({ path: "author", model: User })
      .skip(skipAmount)
      .limit(pageSize)
      .sort(sortOptions);

    const totalQuestions = await Question.countDocuments(query);
    const isNext = totalQuestions > skipAmount + questions.length;
    return { questions, isNext };
  } catch (error) {
    console.log(error);
    throw error;
  }
}

export async function createQuestion(Params: CreateQuestionParams) {
  try {
    // connect to DB
    connectToDatabase();
    const { title, content, tags, author, path } = Params;

    // create a question
    const question = await Question.create({
      title,
      content,
      author,
    });

    const tagDocuments = [];
    // Create the tags or get them if they already exist
    for (const tag of tags) {
      const existingTag = await Tag.findOneAndUpdate(
        // find the existing tag
        { name: { $regex: new RegExp(`^${tag}$`, "i") } },
        // if  I FOUND the existing TAG i will push the question id in questions array in tag document
        // if no matched tag found we will insert a new document with name set the value of the tag
        { $setOnInsert: { name: tag }, $push: { questions: question._id } },
        { upsert: true, new: true }
      );
      tagDocuments.push(existingTag._id);
    }

    // update the question - 1st i will found the question i already create it
    await Question.findByIdAndUpdate(question._id, {
      $push: { tags: { $each: tagDocuments } },
    });

    // Create an interaction record for the user's ask_question action
    await Interaction.create({
      userId: author,
      questionId: question._id,
      action: "ask_question",
      tag: tagDocuments,
    });
    // Increment author's reputation by+5 for creating a question
    await User.findByIdAndUpdate(author, { $inc: { reputation: 5 } });
    // to eliminate the need for a reload
    revalidatePath(path);
  } catch (error) {
    console.log(error);
  }
}

